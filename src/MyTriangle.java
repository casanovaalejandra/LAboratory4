
public class MyTriangle {
	
	private double sideA;
	private double sideB;
	private double sideC;
	public MyTriangle(double firstSide, double secondSide, double thirdSide) {
		sideA=firstSide;
		sideB=secondSide;
		sideC=thirdSide;
	}

	public double getSideA() {
		return sideA;
	}
	public double getSideB() {
		return sideB;
	}
	public double getSideC() {
		return sideC;
	}
	
}
